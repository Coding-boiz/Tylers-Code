public class Library{
  static void print(String h){
    System.out.println(h);
  }
  static void print(int j){
    System.out.println(j);
  }
  static int add(int a, int b){
    return a+b;
  }
  static int sub(int c,int d){
    return(c-d);
  }
  static int div(int e,int f){
    return(e/f);
  }
  static int mul(int g,int h){
    return(g*h);
  }
}
